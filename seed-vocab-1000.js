async function main() {
  console.log('既存の単語を確認中...');
  const existing = await prisma.vocabulary.findMany({ select: { word: true, level: true } });
  const existingSet = new Set(existing.map(v => `${v.level}:${v.word}`));
  
  let added = 0;
  for (const [levelStr, words] of Object.entries(vocab)) {
    const level = parseInt(levelStr);
    const newWords = words.filter(w => !existingSet.has(`${level}:${w.word}`));
    if (newWords.length > 0) {
      await prisma.vocabulary.createMany({
        data: newWords.map(w => ({ word: w.word, meaning: w.meaning, level, category: getCategoryByLevel(level) })),
        skipDuplicates: true,
      });
      added += newWords.length;
      console.log(`Level ${level}: ${newWords.length}単語追加`);
    }
  }
  
  const counts = await prisma.vocabulary.groupBy({ by: ['level'], _count: { id: true }, orderBy: { level: 'asc' } });
  console.log('\n=== 最終単語数 ===');
  counts.forEach(c => console.log(`Level ${c.level}: ${c._count.id}単語`));
  console.log(`合計追加: ${added}単語`);
}

function getCategoryByLevel(level) {
  const cats = { 1: 'basic', 2: 'business', 3: 'intermediate', 4: 'advanced', 5: 'expert' };
  return cats[level] || 'general';
}

main().catch(console.error).finally(() => prisma.$disconnect());
